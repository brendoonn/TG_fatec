    private void carregarPessoa() {
        listaModelPessoa = controllerPessoa.getListaPessoaController();
        DefaultTableModel modelo = (DefaultTableModel) jtPessoa.getModel();
        modelo.setNumRows(0);
        int cont = listaModelPessoa.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelPessoa.get(i).getID_pessoa(),
                listaModelPessoa.get(i).getNome(),
                listaModelPessoa.get(i).getCidade(),
                listaModelPessoa.get(i).getCelular()
            });
        }
    }

