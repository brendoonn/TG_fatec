    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        // TODO add your handling code here:
        int linha = jtPessoa.getSelectedRow();
        int codigoPessoa = (int) jtPessoa.getValueAt(linha, 0);
        if (controllerPessoa.excluirPessoaController(codigoPessoa)) {
            JOptionPane.showMessageDialog(this, "Pessoa excluído com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            this.carregarPessoa();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir o Pessoa!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // TODO add your handling code here:
        int linha = jtPessoa.getSelectedRow();
        int codigoPessoa = (int) jtPessoa.getValueAt(linha, 0);
        salvarAlterar = "alterar";
        modelPessoa = controllerPessoa.getPessoaController(codigoPessoa);
        jtfCodigo.setText(String.valueOf(modelPessoa.getID_pessoa()));
        jtfNome.setText(modelPessoa.getNome());
        jtfSobrenome.setText(modelPessoa.getSobrenome());
        jtfCpf.setText(modelPessoa.getCpf());
        jtfDataDeNascimento.setText(bLdatas.formatDateToString(modelPessoa.getData_nascimento()));
        jtfTelefone.setText(modelPessoa.getCelular());
        jtfEmail.setText(modelPessoa.getEmail());
        jtfCidade1.setText(modelPessoa.getCidade());
        jtfBairro.setText(modelPessoa.getBairro());
        jcbUF.setSelectedItem(modelPessoa.getUf());
        jtfCep.setText(modelPessoa.getCep());
        jtfEndereco.setText(modelPessoa.getRua());
        jtfNumero.setText(String.valueOf(modelPessoa.getNumero()));
        jtfComplemento.setText(modelPessoa.getReferencia());
        this.desabilitaHabilitaCampos(true);

    }//GEN-LAST:event_jbAlterarActionPerformed

