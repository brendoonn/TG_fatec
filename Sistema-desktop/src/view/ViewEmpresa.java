        modelEmpresa.setEmp_complemento(jtfComplemento.getText());
        modelEmpresa.setEmp_cep(jtfCep.getText());
        modelEmpresa.setEmp_bairro(jtfBairro.getText());
        modelEmpresa.setEmp_municipio(jtfMunicipio.getText());
        modelEmpresa.setEmp_uf(jtfUf.getText());
        modelEmpresa.setEmp_telefone(jtfTelefone.getText());
        modelEmpresa.setEmp_imagem(bLMascaras.adicionarBarras(jtfImagem.getText()));
        modelEmpresa.setEmp_logomarca_claro(bLMascaras.adicionarBarras(jtfLogomarca.getText()));
        if (controllerEmpresa.atualizarEmpresaController(modelEmpresa)) {
            JOptionPane.showMessageDialog(this, "Empresa atualizada com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            habilitarDesabilitarCampos(false);
          } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar a empresa!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }        
