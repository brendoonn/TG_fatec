    private void jbImagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbImagemActionPerformed
        JFileChooser arquivo = new JFileChooser();
        arquivo.setDialogTitle("Selecione uma imagem");
        arquivo.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int op = arquivo.showOpenDialog(this);
        if(op == JFileChooser.APPROVE_OPTION){
            imgImagem = new File("");
            imgImagem = arquivo.getSelectedFile();
            FileNameImagem = imgImagem.getAbsolutePath();
            jtfImagem.setText(FileNameImagem);
            ImageIcon imagemBg = new ImageIcon(imgImagem.getPath());
            jlImagem.setIcon(new ImageIcon(imagemBg.getImage().getScaledInstance(jlImagem.getWidth(), jlImagem.getHeight(), Image.SCALE_DEFAULT)));
        }
    }//GEN-LAST:event_jbImagemActionPerformed

